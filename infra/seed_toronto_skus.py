def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Print the cascade plan and the new SKUs, then exit.",
    )
    args = parser.parse_args()

    if args.dry_run:
        _print_plan()
        return 0

    new_ids = [s["sku_id"] for s in TORONTO_SKUS]

    with psycopg.connect(DATABASE_URL, autocommit=False) as conn:
        with conn.cursor() as cur:
            # Idempotency check: if every new SKU is already present and
            # none of the legacy IDs remain, this is a no-op re-run.
            cur.execute(
                "SELECT sku_id FROM skus WHERE sku_id = ANY(%s)",
                (list(LEGACY_SKU_IDS),),
            )
            legacy_present = [r[0] for r in cur.fetchall()]

            cur.execute(
                "SELECT sku_id FROM skus WHERE sku_id = ANY(%s)",
                (new_ids,),
            )
            new_present = {r[0] for r in cur.fetchall()}

            if not legacy_present and len(new_present) == len(new_ids):
                print(
                    f"[seed_toronto_skus] All {len(TORONTO_SKUS)} branded SKUs already "
                    f"present and 0 legacy SKUs left -- nothing to do."
                )
                return 0

            # 1) Delete the dependents that FK to legacy SKUs, in FK-safe order.
            cur.execute(
                "DELETE FROM demand_forecasts WHERE sku_id = ANY(%s)",
                (list(LEGACY_SKU_IDS),),
            )
            forecasts_deleted = cur.rowcount

            cur.execute(
                "DELETE FROM retailer_orders WHERE sku_id = ANY(%s)",
                (list(LEGACY_SKU_IDS),),
            )
            orders_deleted = cur.rowcount

            cur.execute(
                "DELETE FROM production_formulas WHERE sku_id = ANY(%s)",
                (list(LEGACY_SKU_IDS),),
            )
            formulas_deleted = cur.rowcount

            cur.execute(
                "DELETE FROM skus WHERE sku_id = ANY(%s)",
                (list(LEGACY_SKU_IDS),),
            )
            skus_deleted = cur.rowcount

            # 2) Insert the 12 branded SKUs.
            sku_rows = [
                {
                    "sku_id":          s["sku_id"],
                    "name":            s["name"],
                    "category":        s["category"],
                    "margin_per_unit": s["margin_per_unit"],
                    "allergen_tags":   s["allergen_tags"],
                    "shelf_life_days": s["shelf_life_days"],
                }
                for s in TORONTO_SKUS
            ]
            cur.executemany(
                """
                INSERT INTO skus
                  (sku_id, name, category, margin_per_unit, allergen_tags, shelf_life_days)
                VALUES
                  (%(sku_id)s, %(name)s, %(category)s, %(margin_per_unit)s,
                   %(allergen_tags)s, %(shelf_life_days)s)
                ON CONFLICT (sku_id) DO NOTHING
                """,
                sku_rows,
            )

            # 3) Insert the production_formulas rows.
            formula_rows = [
                {"sku_id": s, "ingredient_id": i, "kg_per_unit": k}
                for (s, i, k) in TORONTO_FORMULAS
            ]
            cur.executemany(
                """
                INSERT INTO production_formulas (sku_id, ingredient_id, kg_per_unit)
                VALUES (%(sku_id)s, %(ingredient_id)s, %(kg_per_unit)s)
                ON CONFLICT (sku_id, ingredient_id) DO NOTHING
                """,
                formula_rows,
            )

            # 4) Insert the eight seeded retailer_orders.
            cur.executemany(
                """
                INSERT INTO retailer_orders
                  (retailer_id, sku_id, quantity_units, requested_delivery_date, status)
                VALUES
                  (%(retailer_id)s, %(sku_id)s, %(quantity_units)s,
                   %(requested_delivery_date)s, %(status)s)
                """,
                TORONTO_RETAILER_ORDERS,
            )

        conn.commit()

    print(
        f"[seed_toronto_skus] Cleanup deleted: "
        f"{skus_deleted} skus, {formulas_deleted} formulas, "
        f"{orders_deleted} retailer_orders, {forecasts_deleted} demand_forecasts."
    )
    print(
        f"[seed_toronto_skus] Inserted {len(TORONTO_SKUS)} branded SKUs, "
        f"{len(TORONTO_FORMULAS)} formula rows, "
        f"{len(TORONTO_RETAILER_ORDERS)} retailer orders."
    )
    _summarize_by_brand()
    return 0


def _print_plan() -> None:
    print(f"[seed_toronto_skus] Dry run -- {len(TORONTO_SKUS)} new SKUs:")
    for s in TORONTO_SKUS:
        allergens = ",".join(s["allergen_tags"])
        print(
            f"  {s['sku_id']:42s}  {s['brand']:18s}  "
            f"${s['margin_per_unit']:>4.2f}  shelf={s['shelf_life_days']:>2}d  "
            f"[{allergens}]  {s['name']}"
        )
    print(
        f"[seed_toronto_skus] Cascade plan: DELETE dependents of "
        f"{len(LEGACY_SKU_IDS)} legacy SKUs (demand_forecasts -> retailer_orders "
        f"-> production_formulas -> skus), then INSERT "
        f"{len(TORONTO_SKUS)} skus + {len(TORONTO_FORMULAS)} formulas + "
        f"{len(TORONTO_RETAILER_ORDERS)} retailer_orders."
    )
    _summarize_by_brand()


def _summarize_by_brand() -> None:
    counts = Counter(s["brand"] for s in TORONTO_SKUS)
    line = "  ".join(f"{brand}={n}" for brand, n in sorted(counts.items()))
    print(f"[seed_toronto_skus] By brand: {line}")


if __name__ == "__main__":
    raise SystemExit(main())
