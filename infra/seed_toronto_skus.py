"""Replace the 12 generic synthetic SKUs with the real FGF Brands catalog.

The synthetic seed in infra/supabase/seed.sql carries 12 generic SKU IDs
(sku-blueberry-muffin-4pk, sku-choc-chip-cookie-12pk, ...). FGF Brands
publishes a clear public brand portfolio across six labels:

  - ACE Bakery       -- baguettes, focaccia, ciabatta, sourdough
                       (acebakery.com -- product mentions verified 2026-05)
  - Stonefire        -- naan, naan dippers, mini naan, pizza crusts
                       (stonefire.com -- product mentions verified 2026-05)
  - Wonder Bread     -- sliced classic loaves
                       (City of Pickering / Baking Business press releases)
  - D'Italiano       -- buns and rolls (same source)
  - Country Harvest  -- whole-grain sliced breads (same source)
  - Casa Mendosa     -- tortillas (same source)

This script swaps in 12 real branded SKUs with realistic categories,
allergens, margins, and shelf life, and rewires production_formulas +
the seeded retailer_orders to reference them.

Cascade (the new IDs are different from the old, so this is destructive):
  1. DELETE demand_forecasts rows pointing at old SKUs
       (runtime-generated by F2.8; will regenerate next forecast pass)
  2. DELETE seeded retailer_orders rows pointing at old SKUs
       (the 8 rows from infra/supabase/seed.sql)
  3. DELETE production_formulas rows pointing at old SKUs
       (the ~60 rows from infra/supabase/seed.sql)
  4. DELETE the 12 old skus rows themselves
  5. INSERT the 12 new branded skus
  6. INSERT the new production_formulas (3-6 ingredients per SKU; only
     references ingredients already in the seed -- no new ingredients)
  7. INSERT the 8 retailer_orders, mapped to keep the retailer-tier mix
     (Costco bulk staples, Walmart staples, Loblaws mid, Whole Foods premium)

The script is idempotent: re-running after a successful pass is a no-op
because the old SKU IDs no longer exist.

Usage:
  uv run infra/seed_toronto_skus.py             # apply cascade
  uv run infra/seed_toronto_skus.py --dry-run   # print plan, do not write
"""

